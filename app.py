LOG_FILE = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'audit_log.json')

def read_logs():
    if not os.path.exists(LOG_FILE):
        return []
    try:
        with open(LOG_FILE, 'r', encoding='utf-8') as f:
            content = f.read().strip()
            if not content:
                return []
            return json.loads(content)
    except Exception:
        return []

def write_log(entry):
    logs = read_logs()
    logs.append(entry)
    try:
        with open(LOG_FILE, 'w', encoding='utf-8') as f:
            json.dump(logs, f, indent=2)
    except Exception as e:
        app.logger.error(f"Failed to write to log file: {e}")

def update_log_status_and_appeal(submission_id, appeal_obj):
    logs = read_logs()
    found = False
    for entry in logs:
        if entry.get("content_id") == submission_id or entry.get("submission_id") == submission_id:
            entry["status"] = "under_review"
            entry["appeal"] = appeal_obj
            found = True
            break
    if found:
        try:
            with open(LOG_FILE, 'w', encoding='utf-8') as f:
                json.dump(logs, f, indent=2)
            return True
        except Exception as e:
            app.logger.error(f"Failed to update log file: {e}")
            return False
    return False

def calculate_linguistic_complexity(text):
    """
    Computes local linguistic complexity based on sentence length variance (burstiness)
    and Type-Token Ratio (TTR - lexical density).
    Returns a score S2 in [0.0, 1.0].
    - 1.0 represents extreme uniformity (low sentence length variance, highly repetitive vocabulary).
    - 0.0 represents highly variable sentence structures and diverse vocabulary.
    """
    if not text or not text.strip():
        return 1.0

    sentence_delimiters = re.compile(r'[.!?]+')
    raw_sentences = sentence_delimiters.split(text)
    
    sentence_lengths = []
    all_words = []
    
    for s in raw_sentences:
        s_clean = s.strip()
        if not s_clean:
            continue
        words = [re.sub(r'[^\w\s]', '', w).lower() for w in s_clean.split()]
        words = [w for w in words if w]
        if words:
            sentence_lengths.append(len(words))
            all_words.extend(words)
            
    if not all_words:
        return 1.0
        
    total_tokens = len(all_words)
    unique_tokens = len(set(all_words))
    ttr = unique_tokens / total_tokens if total_tokens > 0 else 0.0
    
    num_sentences = len(sentence_lengths)
    if num_sentences > 1:
        mean_len = sum(sentence_lengths) / num_sentences
        variance = sum((x - mean_len) ** 2 for x in sentence_lengths) / num_sentences
    else:
        variance = 0.0
        
    s_var = math.exp(-variance / 20.0)
    
    s_ttr = 1.0 - ttr
    
    s2 = 0.5 * s_var + 0.5 * s_ttr
    
    return max(0.0, min(1.0, s2))

def calculate_llm_stylistic_probability(text):
    """
    Sends the text to the Groq API using llama-3.3-70b-versatile with a structured prompt.
    Returns a score S1 in [0.0, 1.0] representing the probability that the text is AI-generated.
    Falls back to None on failure to ensure system resilience.
    """
    api_key = os.environ.get("GROQ_API_KEY")
    if not api_key:
        print("Groq API key not found in environment.")
        return None

    try:
        client = Groq(api_key=api_key)
        
        system_prompt = (
            "You are an expert stylometrist analyzing text to determine if it was generated by an AI language model. "
            "Evaluate token predictability, sentence length variation patterns, paragraph transitions, "
            "and characteristic AI terms (e.g., 'delve', 'testament', 'tapestry', 'furthermore', 'monumental', 'crucial'). "
            "Respond ONLY with a valid JSON object containing a single key 'ai_probability' which is a float "
            "between 0.0 and 1.0 (where 1.0 is definitely AI-generated and 0.0 is definitely human-authored). "
            "Do not include any explanation or markdown formatting other than JSON."
        )
        
        user_prompt = f"Analyze the following text:\n\n{text}"
        
        chat_completion = client.chat.completions.create(
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ],
            model="llama-3.3-70b-versatile",
            temperature=0.0,
            response_format={"type": "json_object"}
        )
        
        content = chat_completion.choices[0].message.content
        res = json.loads(content)
        prob = float(res.get("ai_probability", 0.5))
        return max(0.0, min(1.0, prob))
    except Exception as e:
        print(f"Groq API error: {e}")
        return None

def combine_signals_and_calibrate(s1, s2):
    """
    Combines Signal 1 (Groq LLM Probability, w1 = 0.6) and Signal 2 (Local Complexity, w2 = 0.4).
    If Signal 1 is not available (None), falls back entirely to Signal 2.
    
    Returns:
      - p_ai: Combined probability score in [0.0, 1.0]
      - attribution: Class label ('likely_human', 'likely_ai', or 'uncertain')
      - confidence: Calibrated confidence score in [0.0, 1.0]
    """
    if s1 is None:
        p_ai = s2
    else:
        p_ai = 0.6 * s1 + 0.4 * s2
        
    p_ai = max(0.0, min(1.0, p_ai))
    
    if p_ai >= 0.80:
        attribution = "likely_ai"
        confidence = p_ai
    elif p_ai <= 0.20:
        attribution = "likely_human"
        confidence = 1.0 - p_ai
    else:
        attribution = "uncertain"
        confidence = 1.0 - (abs(p_ai - 0.50) / 0.30)
        confidence = max(0.0, min(1.0, confidence))
        
    return p_ai, attribution, confidence

