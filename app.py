@app.route("/submit", methods=["POST"])
@app.route("/api/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json() or {}
    text = data.get('text') or data.get('content') or ""
    creator_id = data.get('creator_id', 'anonymous')
    
    if not text:
        return jsonify({"error": "Missing required field 'text' or 'content'"}), 400

    s1 = calculate_llm_stylistic_probability(text)
    s2 = calculate_linguistic_complexity(text)
    
    p_ai, attribution, confidence = combine_signals_and_calibrate(s1, s2)
    
    conf_pct = int(round(confidence * 100))
    if attribution == "likely_ai":
        label = f"AI-Generated: Our analysis indicates with high confidence ({conf_pct}% similarity to machine patterns) that this content was generated by an AI system. It exhibits highly predictable patterns, uniform sentence lengths, and stylistic markers characteristic of machine-generated text."
    elif attribution == "likely_human":
        label = f"Verified Human: Our analysis indicates with high confidence ({conf_pct}% similarity to human patterns) that this content was authored by a human. It shows natural linguistic variation and complex sentence structures typical of human creativity."
    else:
        label = f"Mixed or Uncertain: Our analysis is unable to definitively attribute this content (uncertainty index {conf_pct}%). It exhibits a blend of natural human style and structured machine patterns. This could indicate human text that has been heavily AI-edited, or AI text designed to mimic human writing."

    content_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S.%f")[:-3] + "Z"
    
    log_entry = {
      "content_id": content_id,
      "submission_id": content_id,
      "creator_id": creator_id,
      "timestamp": timestamp,
      "attribution": attribution,
      "confidence": round(confidence, 2),
      "llm_score": round(s1, 2) if s1 is not None else None,
      "signals": {
          "local_complexity": round(s2, 2),
          "llm_stylistic": round(s1, 2) if s1 is not None else None
      },
      "label": label,
      "status": "classified",
      "appeal": None
    }
    
    write_log(log_entry)
    
    return jsonify({
        "content_id": content_id,
        "submission_id": content_id,
        "attribution": attribution,
        "confidence": round(confidence, 2),
        "label": label,
        "signals": {
            "local_complexity": round(s2, 2),
            "llm_stylistic": round(s1, 2) if s1 is not None else None
        },
        "status": "classified",
        "created_at": timestamp
    }), 200

@app.route("/appeal", methods=["POST"])
@app.route("/api/appeal", methods=["POST"])
def appeal():
    data = request.get_json() or {}
    submission_id = data.get("content_id") or data.get("submission_id")
    reasoning = data.get("creator_reasoning") or data.get("reasoning")
    
    if not submission_id or not reasoning:
        return jsonify({"error": "Missing required field 'content_id' or 'creator_reasoning'"}), 400
        
    if len(reasoning) < 10:
        return jsonify({"error": "Reasoning must be at least 10 characters long"}), 400
        
    appeal_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S.%f")[:-3] + "Z"
    
    appeal_obj = {
        "appeal_id": appeal_id,
        "reasoning": reasoning,
        "creator_reasoning": reasoning,
        "created_at": timestamp
    }
    
    success = update_log_status_and_appeal(submission_id, appeal_obj)
    if not success:
        return jsonify({"error": f"Submission with ID {submission_id} not found"}), 404
        
    return jsonify({
        "appeal_id": appeal_id,
        "submission_id": submission_id,
        "content_id": submission_id,
        "status": "under_review",
        "reasoning": reasoning,
        "created_at": timestamp
    }), 200

